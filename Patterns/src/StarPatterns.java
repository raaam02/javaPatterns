
public class StarPatterns {
	public static void main(String[] args) {
		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j < i; j++) {
				System.out.print("* ");
			}
			System.out.println(" ");
		}
		System.out.println("For Update Check by 2nd Branch");
		System.out.println("For Update Check by 2nd Branch");
		System.out.println("For Update Check by 3nd Branch");

	}
}
